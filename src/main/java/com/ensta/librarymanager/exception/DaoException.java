package com.ensta.librarymanager.exception;

public class DaoException extends Exception {

    public DaoException() {
        super();
    }

    public DaoException(java.lang.String arg0) {
        super(arg0);
    }

    public DaoException(java.lang.String arg0, java.lang.Throwable arg1) {
        super(arg0, arg1);
    }

    public DaoException(java.lang.Throwable arg0) {
        super(arg0);
    }

}
